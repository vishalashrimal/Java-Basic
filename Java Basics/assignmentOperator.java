import java.util.*;

public class assignmentOperator {
    public static void main(String args[]) {
        int a = 10;
        a += 13;
        System.out.println(a);
    }
}

/*
 * Assigment Operator :-
 * [+=] eg. a+= 5 || a=a+5
 * [-=] eg. a-= 5 || a=a-5
 * [*=] eg. a*= 5 || a=a*5
 * [/=] eg. a\= 5 || a=a\5
 * 
 */