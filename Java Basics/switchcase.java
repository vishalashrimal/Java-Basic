import java.util.*;
public class switchcase {
    public static void main(String args[]){
        int number = 2;
        switch (number){
            case 1 : System.out.println("Samosa");
                        break;
            case 2 : System.out.println("Burger");
                        break;
            case 3 : System.out.println("Mangoshake");
                        break;
            default : System.out.println("We wake up");            
        }
    }
}
