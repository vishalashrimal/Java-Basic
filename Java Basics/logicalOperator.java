import java.util.*;

public class logicalOperator {
    public static void main(String args[]) {
        int a = 10;
        int b = 20;
        System.out.println(a > b && b < a);
        System.out.println((a > b) || (a < b));
    }
}

/*
 * Logical Operator :-
 * [ &&] - Logical And
 * [||] - Logical Or
 * [ !] - Logical Not
 */